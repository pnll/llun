                                    <p>null [n*l] a.<br>1 무효의(useless); 무익한; 중요하지 않은, 무의미의<br>2 【컴퓨터】 빈 ((정보의 부재))<br>
				    3 특징[개성]이 없는, 무표정의<br>
			4 존재하지 않는, 하나도 없는; 【수학】 영(零)의<br>~ and void 【법】 무효인<br>n. 영, 제로; 영도(零度)<br>vt. 영으로 하다 ((out)); 무효화하다</p>
                        <p>널 [ null, NUL ]<br>①정보의 부재. 
                        0이나 공백과는 달리 정보가 없음을 나타낸다.<br>②영(0).<br>③중앙 또는 끝 위치에서 아무런 편차가 없는 것.<br>
			④무연산(no-operation)과 같은 뜻으로 쓰이는 말.</p>
                        <p>널값 [ null value ]<br>데이터베이스에서 관계의 속성이나 레코드 항목의 값이 
                        실제로 존재할 수 없거나, 존재하는지의 여부를 알 수 없거나, 
                        존재할 
                        수는 있으나 그 값을 알 수 없거나, 값이 존재하다가  안 
                        하다가 하는 것과 같은 상태를 나타내기 위해 설정되는 
                        값. </p>
                        <p>null 널<br>1 정보가 없거나, 있더라도 정당한 정보가 아님을 나타내는 
                        말.<br>2 아무것도 없음, 혹은 0의 뜻을 내포하는 말.</p>
                        <p>'NULL'은 사전적으로 &quot;무효의&quot;, &quot;존재하지 
                        않는&quot; 의미를 &nbsp;지니고 있습니다. 오라클 데이터베이스에서는 
                        'NULL'은  &quot;아직 확정되지 않은 값&quot;을 의미합니다.<br>이 의미는 무한한 가능성을 내포하고 있으며 빈 그릇은 
                        모든 것을 담을 수 있으므로 모든 것을 수용할 수 있다는 
                        뜻도  내포되어 있습니다.</p>
